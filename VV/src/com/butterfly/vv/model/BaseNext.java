package com.butterfly.vv.model;

public abstract class BaseNext {
	private String nextOffline;
	private String nextOnline;

	public String getNextOffline() {
		return nextOffline;
	}
	public void setNextOffline(String nextOffline) {
		this.nextOffline = nextOffline;
	}
	public String getNextOnline() {
		return nextOnline;
	}
	public void setNextOnline(String nextOnline) {
		this.nextOnline = nextOnline;
	}
}
